/* A graph data structure consists of a finite set of vertices (or nodes) and a set of 
edges connecting these vertices. The vertices represent objects or entities and the edges
 represent relationships or connections between these objects. Graphs can be used to
  represent a wide variety of information, such as social networks, transportation systems,
   and web pages. They can be represented in a variety of ways, such as adjacency lists or 
   matrices, and can be analyzed using various algorithms, such as search and traversal
    algorithms.
 */


/*There are several key terms and concepts used when talking about graph data structures:

    Vertex (or node): a basic unit of a graph, representing an object or entity.
    Edge: a connection between two vertices, representing a relationship or connection between the objects or entities represented by those vertices.
    Degree: the number of edges connected to a vertex.
    Path: a sequence of vertices connected by edges.
    Cycle: a path that starts and ends at the same vertex.
    Connected: a graph is considered connected if there is a path between every pair of vertices.
    Directed graph (or digraph): a graph in which edges have a direction, such as from vertex A to vertex B.
    Undirected graph: a graph in which edges do not have a direction.
    Weighted graph: a graph in which edges have a value or weight associated with them.
    Adjacency matrix: a matrix representation of a graph, where the rows and columns represent vertices and the entries represent the presence or absence of an edge.
    Adjacency list: a list representation of a graph, where each vertex is associated with a list of its adjacent vertices. 
 */
