/* In computer science, a tree is a widely used abstract data type that simulates a 
hierarchical tree structure, with a set of nodes and a set of edges. A tree data structure
 can be defined recursively (locally) as a collection of nodes (starting at a root node),
  where each node is a data structure consisting of a value, together with a list of 
  references to nodes (the "children"), with the constraints that no reference is duplicated,
   and none points to the root. */
 
//    A tree can be visualized as shown below:

   /*

          A
        /   \
       B     C
      / \   / \
     D   E F   G


   */
   
 /* In this example, the tree has a root node (A) and three levels. Node A is the parent
  of nodes B and C, and B and C are the children of node A. Similarly, nodes D and E are
   children of node B, and nodes F and G are children of node C. */
 

